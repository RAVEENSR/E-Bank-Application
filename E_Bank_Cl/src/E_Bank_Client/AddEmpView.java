    private boolean checkFields(){
        boolean flag=true;
        if(jTxtName.getText().equals("")){
            flag= false;
        }else if(jTxtPosition.getText().equals("")){
            flag= false;
        }
        else if(jTxtUserName.getText().equals("")){
            flag= false;
        }
        else if(jPwdPassword.getPassword().equals("")){
            flag= false;
        }
        return flag;
    }
