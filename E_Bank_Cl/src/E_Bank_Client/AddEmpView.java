    private void jBtnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddEmpActionPerformed
        //check the fields and send them to web service
        //web service should check wheter the account number is already available
            if(checkFields()){
                String name=jTxtName.getText();
                String position=jTxtPosition.getText();
                String username=jTxtUserName.getText();
                String password=String.valueOf(jPwdPassword.getPassword());
                boolean flag=registerEmployee(name,position,username,password);
                if(!flag){
                    JOptionPane.showMessageDialog(this, "Error! Try again with correct data");
                    //jLblMessage.setText("Error! Try again with correct data");
                }else{
                    //jLblMessage.setText("Employee added successfully!");
                    JOptionPane.showMessageDialog(this, "Employee added successfully!");
                } 
            }else{
                JOptionPane.showMessageDialog(this, "Please fill all the fields!");
            }
        
    }//GEN-LAST:event_jBtnAddEmpActionPerformed

