    private boolean checkFields(){
        boolean flag=true;
        if(jTxtCusName.getText().equals("")){
            flag= false;
        }
        else if(jTxtBDay.getText().equals("")){
            flag= false;
        }
        else if(jTxtAddress.getText().equals("")){
            flag= false;
        }
        else if(jTxtMobile.getText().equals("")){
            flag= false;
        }
        else if(jTxtEmail.getText().equals("")){
            flag= false;
        }
        else if(jTxtAccNum.getText().equals("")){
            flag= false;
        }
        else if(jTxtSortCode.getText().equals("")){
            flag= false;
        }
        else if(jTxtBalance.getText().equals("")){
            flag= false;
        }
        else if(jTxtCard.getText().equals("")){
            flag= false;
        }
        else if(jComboAccType.getSelectedItem().equals("")){
            flag= false;
        }
        
        return flag;
    }
    
