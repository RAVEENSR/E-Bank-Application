    private void jBtnEditCusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditCusActionPerformed

            if(checkFields()){
                try{
                    String name=jTxtCusName.getText();
                    String bday=jTxtBDay.getText();
                    String address=jTxtAddress.getText();
                    int mobile=Integer.valueOf(jTxtMobile.getText());
                    String accType=jComboAccType.getSelectedItem().toString();
                    String email=jTxtEmail.getText();
                    int accNum=Integer.valueOf(jTxtAccNum.getText());
                    String sortCode=jTxtSortCode.getText();
                    int balance=Integer.valueOf(jTxtBalance.getText());
                    String cardNum=jTxtCard.getText();
                    boolean flag=editClient(name,bday,address,mobile,email,accNum,accType,sortCode,balance,cardNum);
                    if(!flag){
                        JOptionPane.showMessageDialog(this, "Error! Try again with correct data");
                        //jLblMessage.setText("Error! Try again with correct data");
                    }else{
                        JOptionPane.showMessageDialog(this, "Customer added successfully!");
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this, "Mobile, Account Number, Balance fields are only allowed digits only");
                }
                       
            }else{
                JOptionPane.showMessageDialog(this, "Please fill all the fields with valid data!");
            }
        
    }//GEN-LAST:event_jBtnEditCusActionPerformed

